#include<stdio.h>
int main()
{

/* if condition true='1' or condition false='0'*/
 int a=5,b=10;
 
 //greater than
 printf("%d\n",a>-1);
 printf("%d\n",a>5);
 
//greater than or equal to 
 printf("%d\n",a>=5);
 printf("%d\n",a>=b);
 
 //less than operator
 
 printf("%d\n",a<b);
 printf("%d\n",a<5);
 
 //less than or equal to
 
 printf("%d\n",a<=5);
 printf("%d\n",a<=1);
 
 //equal to operator
 
 printf("%d\n",a==5);
 printf("%d\n",a==b);
 
 //not equal to
 printf("%d\n",a!=5);
 printf("%d\n",a!=7);
 
  

  return 0;
  
}  

